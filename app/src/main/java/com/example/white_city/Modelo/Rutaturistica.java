package com.example.white_city.Modelo;

public class Rutaturistica {
    private String id;
    private String ruta_turistica;
    private String descripcion;
    private String municipio_ubicada;
    private String direccion_ruta;
    private String contactos;
    private String h_apertura;
    private String h_cierre;
    private String tipo_rutaTur;
    private String imagen;

    public Rutaturistica(){

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRuta_turistica() {
        return ruta_turistica;
    }

    public void setRuta_turistica(String ruta_turistica) {
        this.ruta_turistica = ruta_turistica;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getMunicipio_ubicada() {
        return municipio_ubicada;
    }

    public void setMunicipio_ubicada(String municipio_ubicada) {
        this.municipio_ubicada = municipio_ubicada;
    }

    public String getDireccion_ruta() {
        return direccion_ruta;
    }

    public void setDireccion_ruta(String direccion_ruta) {
        this.direccion_ruta = direccion_ruta;
    }

    public String getContactos() {
        return contactos;
    }

    public void setContactos(String contactos) {
        this.contactos = contactos;
    }

    public String getH_apertura() {
        return h_apertura;
    }

    public void setH_apertura(String h_apertura) {
        this.h_apertura = h_apertura;
    }

    public String getH_cierre() {
        return h_cierre;
    }

    public void setH_cierre(String h_cierre) {
        this.h_cierre = h_cierre;
    }

    public String getTipo_rutaTur() {
        return tipo_rutaTur;
    }

    public void setTipo_rutaTur(String tipo_rutaTur) {
        this.tipo_rutaTur = tipo_rutaTur;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
}
