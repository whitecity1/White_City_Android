package com.example.white_city.Modelo;

public class Recomendado {
    private String idrecomendado;
    private String lugar_recomendado;
    private String calificaciones;
    private String resenahistorica;
    private String user_id;
    private String imagen;

    public Recomendado(){

    }

    public String getIdrecomendado() { return idrecomendado; }

    public void setIdrecomendado(String idrecomendado) { this.idrecomendado = idrecomendado; }

    public String getLugar_recomendado() { return lugar_recomendado; }

    public void setLugar_recomendado(String lugar_recomendado) { this.lugar_recomendado = lugar_recomendado; }

    public String getCalificaciones() { return calificaciones; }

    public void setCalificaciones(String calificaciones) { this.calificaciones = calificaciones; }

    public String getResenahistorica() { return resenahistorica; }

    public void setResenahistorica(String resenahistorica) { this.resenahistorica = resenahistorica; }

    public String getUser_id() { return user_id; }

    public void setUser_id(String user_id) { this.user_id = user_id; }

    public String getImagen() { return imagen; }

    public void setImagen(String imagen) { this.imagen = imagen; }
}
